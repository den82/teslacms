// Getting other languages which is no equal code. Putting languages in array.
function get_other_langs($conn, $code) {
   $sql = "SELECT * FROM cms_languages WHERE code != '".$code."' ORDER BY id ASC";
   $result = mysqli_query($conn, $sql);
   if (!$result)
     return false;
   $count_rows = mysqli_num_rows($result);
   if ($count_rows < 1)
      return false;  
   $result = mysqli_fetch_all($result, MYSQLI_ASSOC);
   return $result;
}

// Getting language name by code from languages table
function get_lang_name_by_code($conn, $code) {
	$sql = "SELECT name FROM cms_languages WHERE code = '".$code."'"; 
	$result = mysqli_query($conn, $sql);
	if (!$result)
	 return false;
	$count_rows = mysqli_num_rows($result);
	if ($count_rows < 1)
	  return false;
	$result = mysqli_fetch_assoc($result);
	return $result['name'];
}